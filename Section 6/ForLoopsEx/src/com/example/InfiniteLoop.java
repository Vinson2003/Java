
package com.example;

public class InfiniteLoop {
    public static void main(String[] args) {
   
    
	// The for loop will run infinite times.
    for(int i = 1; i<=5; i++){
        System.out.println(i + ". Hello");
    }
     
    // To terminate this program press ctrl + c in the console.
  }
} 
    

